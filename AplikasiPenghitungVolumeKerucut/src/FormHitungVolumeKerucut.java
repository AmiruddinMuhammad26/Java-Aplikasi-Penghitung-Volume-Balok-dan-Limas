    private void btnhitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhitungActionPerformed
        // TODO add your handling code here:
        double jari2, tinggi, luas;
        if (txtjari.getText().equals("")&& txttinggi.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Pastikan Semua Data Terisi!!!");
            txtjari.requestFocus();
        } else if (txtjari.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Masukkan Panjang Jari-jari Kerucut!!!");
            txtjari.requestFocus();
        }else if (txttinggi.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Masukkan Tinggi Kerucut !!!");
            txttinggi.requestFocus();
        } else {
            jari2=Double.parseDouble(txtjari.getText());
            tinggi=Double.parseDouble(txttinggi.getText());
            luas=(1.0/3)*(3.14)*Math.pow(jari2, 2)*tinggi;
            txtvolume.setText(String.valueOf(luas));
        }
    }//GEN-LAST:event_btnhitungActionPerformed

